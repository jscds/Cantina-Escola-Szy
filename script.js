// Declarando a variável com o nome da cantina
var nomeCantina = "Cantina da Escola";
console.log("Bem-vindo à " + nomeCantina);

// Declarando a quantidade de salgados disponíveis
let Rizoles = 20;
console.log("Temos " + Rizoles + " salgados disponíveis.");

// Declarando o preço fixo de cada salgado
const precoSalgado = 5;
console.log("Cada salgado custa R$" + precoSalgado);

//Atualizando o numero de salgados
Rizoles = Rizoles - 5;
let totalVendido = 5 * precoSalgado;

console.log("Agora restam " + Rizoles + " Rizoles.");
console.log("A cantina vendeu R$" + totalVendido);
